#include "libft.h"

char	*ft_strcat(char *destination, const char *source)
{
	char	*ptr;

	ptr = destination + ft_strlen(destination);
	while (*source != '\0')
	{
		*ptr++ = *source++;
	}
	*ptr = '\0';
	return (destination);
}
