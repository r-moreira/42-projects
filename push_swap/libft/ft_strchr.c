#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*buffer_str;

	buffer_str = (char *)s;
	while (*buffer_str != c)
	{
		if (!*buffer_str)
			return (NULL);
		buffer_str++;
	}
	return (buffer_str);
}
